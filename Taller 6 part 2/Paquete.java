public class Paquete {
    private double peso;
    private double altura;
    private double base;
    public Paquete(double peso, double altura, double base) {
        this.peso = peso;
        this.altura = altura;
        this.base = base;
    }

    public double getPeso() {
        return peso;
    }

    public double getAltura() {
        return altura;
    }

    public double getBase() {
        return base;
    }
}
